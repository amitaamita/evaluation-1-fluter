// Définition de la fonction addition
int addition(int a, int b) {
  return a + b; // Retourne la somme des deux nombres
}

void main() {
  // Appel de la fonction addition avec les nombres 5 et 7
  int resultat = addition(5, 7);

  // Affichage du résultat
  print("La somme de 5 et 7 est: $resultat");
}
